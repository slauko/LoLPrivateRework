float Cassiopeia::ESpeed = HUGE_VALF;
float Cassiopeia::RSpeed = HUGE_VALF;

float Cassiopeia::QDelay = 0.f;
float Cassiopeia::WDelay = 0.f;
float Cassiopeia::EDelay = 0.f;
